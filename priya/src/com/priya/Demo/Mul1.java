package com.priya.Demo;

public class Mul1 
{
	public static void main(String[] args) 
	{
	 System.out.print("Multiplication table\n");
	 int i=1;
	 while(i<=10)
	 {
		int j=1;
		 while(j<=10)
		 {
			 System.out.println(i + " * " + j +  "="  + i*j+"\n");
			 j++;
	 }	 
		 i++;
		 System.out.print("\n...................................................\n");
		
	 }
	}}

